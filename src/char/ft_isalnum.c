#include "libft.h"

int	ft_isalnum(int c)
{
	int	ret;

	ret = 0;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z')
	    || (c >= '0' && c <= '9')) {
		ret = 1;
	}
	return (ret);
}
